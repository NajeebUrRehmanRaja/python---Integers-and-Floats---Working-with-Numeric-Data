# Basic arithmetic operations

# Addition
print("3 + 2 =", 3 + 2)

# Subtraction  
print("3 - 2 =", 3 - 2)

# Multiplication
print("3 * 2 =", 3 * 2)

# Division
print("3 / 2 =", 3 / 2)

# Floor Division
print("3 // 2 =", 3 // 2)

# Exponent
print("3 ** 2 =", 3 ** 2)

# Modulus
print("3 % 2 =", 3 % 2)